class ArithmeticOperator
{
	public static void main(String[] args)
	{
		int a=10,b=2;
		int res1=a+b;
		int res2=a-b;
		int res3=a*b;
		int res4=a/b;
		int res5=a%b;
		System.out.println(res1);
		System.out.println(res2);
		System.out.println(res3);
		System.out.println(res4);
		System.out.println(res5);
	}
}
